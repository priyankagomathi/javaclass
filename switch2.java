class switch2
{
public static void main(String[]args)
{
int a=3;
switch(a)
{
case 1:
System.out.println("this is first statement");
break;

case 2:
System.out.println("this is second statement");
break;

case 3:
System.out.println("this is third statement");
break;

default:
System.out.println("none");
}
}
}